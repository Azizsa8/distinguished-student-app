export function getDynamicComment(field, value) {
  if (!value) return null;
  return COMMENTS[field]?.[value] || null;
}

export function getCareerComment(text) {
  if (!text || text.length < 30) return null;
  if (text.length > 200) return 'خطتك التفصيلية تدل على نضج فكري وبصيرة استراتيجية — هذا الوضوح في الرؤية أساس للنجاح.';
  if (text.length > 80) return 'طموحك الواضح دليل على وعي مبكر بمستقبلك — هذه الرؤية ستوجه مسيرتك.';
  return 'الخطة الوظيفية الواضحة أولى خطوات النجاح — واصل التفكير في مستقبلك.';
}

